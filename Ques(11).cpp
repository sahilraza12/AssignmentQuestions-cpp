#include <iostream>

int main() {
    char a, b, c;
    std::cout << "Enter three characters: ";
    std::cin >> a >> b >> c;
    std::cout << a << " -> " << (int)a << std::endl;
    std::cout << b << " -> " << (int)b << std::endl;
    std::cout << c << " -> " << (int)c << std::endl;
    return 0;
}


